public class Ouro extends Entidade {
    
    public Ouro(int posX, int posY){
        this.nome = "O";
        this.sensacao = "G";
        this.posX = posX;
        this.posY = posY;
    }
    public int getPosX() {
        return posX;
    }
    public int getPosY() {
        return posY;
    }
}
