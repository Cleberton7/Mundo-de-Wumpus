public class Poco extends Entidade {
    public Poco(int posX, int posY){
        this.nome="P";
        this.sensacao="B";
        this.posX = posX;
        this.posY = posY;
    }
    public int getPosX() {
        return posX;
    }
    public int getPosY() {
        return posY;
    }
    
}
